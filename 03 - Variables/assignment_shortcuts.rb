a = 10
# a = a + 5
# Another shortcut
a += 5 # also means a = a +5
puts a

b = 100
# b = b - 40
b -= 40
puts b

c = 3
# c = c * 4
c *= 4
puts c

name = "Ahmed"
name += " The Great" # means concatenate
