# max - greatest value in an array
# min - smallest value in an array
# for an empty array both returns nil
stock_prices = [434.12, 723.99, 84.12, 649.92]
p stock_prices.max
p stock_prices.min

companies = ["Nike", "Microsfot", "Apple"]
p companies.max
p companies.min

# So the string that is closest to the beginning of the alphabet is going to be
# considered the smallest one.And the string that is closest to the end of the
# alphabet will be considered the largest one.

p [].max
p [].min
